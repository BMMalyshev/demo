package Cycles07;

public class For {
    public static void main(String[] args) {
//        System.out.println("Before for");
//        for(int i = 0 ; i < 5; i++){
//            System.out.println(i);
//        }
//        System.out.println("After for");
            for (int i = 0; i < 5; i++) {
                System.out.print(i + " ");
            }
            System.out.println(" ");

            for (int i = 5; i > 0; i--) {
                System.out.print(i + " ");
            }
            System.out.println(" ");

            for (int i = 0; i < 100; i+=10) {
                System.out.print(i + " ");
            }
            System.out.println(" ");

    }
}
